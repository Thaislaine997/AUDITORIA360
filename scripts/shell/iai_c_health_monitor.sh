            -H "Authorization: token $GITHUB_TOKEN" \
            -H "Accept: application/vnd.github.v3+json" \
            "https://api.github.com/repos/$REPO_OWNER/$REPO_NAME/issues" \
            -d '{
                "title": "'"$title"'",
                "body": "'"$escaped_body"'",
                "labels": ['"$labels"']
            }' > /dev/null
        
        echo -e "${GREEN}âœ… Issue created successfully${NC}"
    else
        echo -e "${YELLOW}âš ï¸  GITHUB_TOKEN not available - saving issue content locally${NC}"
        
        # Save to local file
        issue_file="$PROJECT_ROOT/automated_issues_$(date +%Y%m%d_%H%M%S).md"
        echo "# $title" > "$issue_file"
        echo "" >> "$issue_file"
        echo "$body" >> "$issue_file"
        echo "" >> "$issue_file"
        echo "Labels: $labels" >> "$issue_file"
        
