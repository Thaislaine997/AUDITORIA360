# Function to create GitHub issue via API (if token is available)
create_github_issue() {
    local title="$1"
    local body="$2"
    local labels="$3"
    
    if [ -n "$GITHUB_TOKEN" ]; then
        echo -e "${BLUE}ğŸ“ Creating GitHub issue: $title${NC}"
        
